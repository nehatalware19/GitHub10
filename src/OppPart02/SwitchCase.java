package OppPart02;

public class SwitchCase {

	public static void main(String[] args) {
	
		String City = "Mumbai";
		
		switch(City) {
		
		case "Mumbai":
			System.out.println("Hello I have reached in Mumbai");
			break;
			
		case "Pune":
			System.out.println("Hello I have reached in Pune");
			break;
			
			
			
			
		case "Delhi":
			System.out.println("Hello I have reached in Delhi");
			break;
			
			
			
			
			
		case "Kalkatta":
			System.out.println("Hello I have reached in Kalkatta");
			break;
			
		}

	}

}
